class MainContainer extends StatefulWidget {
  const MainContainer({super.key});

  @override
  State<MainContainer> createState() => _MainContainerState();
}

class _MainContainerState extends State<MainContainer> {
  int _currentIndex = 0;
  String _userName = 'Guest User';
  dynamic _userListener;
  bool _isAdmin = false;

  // Use IndexedStack to preserve state of screens (especially the live map screen)
  final List<Widget> _screens = [
    const ReportIssueScreen(),
    const LiveMapScreen(),
    const InsightsScreen(),
    const AdminDispatchScreen(),
  ];

  @override
  void initState() {
    super.initState();
    _loadUser();
  }

  @override
  void dispose() {
    _userListener?.cancel();
    super.dispose();
  }

  void _loadUser() {
    try {
      final user = FirebaseAuth.instance.currentUser;
      if (user != null) {
        setState(() {
          _userName = user.displayName ?? (user.isAnonymous ? 'Guest User' : 'Citizen');
        });
        
        // Listen to live updates from Firestore
        _userListener = FirebaseFirestore.instance.collection('users').doc(user.uid).snapshots().listen((doc) {
          if (doc.exists && doc.data() != null && mounted) {
            final data = doc.data()!;
            setState(() {
              if (data['name'] != null) {
                _userName = data['name'];
              }
              _isAdmin = data['role'] == 'admin';
            });
          }
        });
      }
    } catch (e) {
      debugPrint('Firebase not initialized in MainContainer: $e');
    }
  }

  @override
  Widget build(BuildContext context) {
    final screens = _isAdmin ? _screens : _screens.sublist(0, 3);

    return Scaffold(
      body: Stack(
        children: [
          IndexedStack(
            index: _currentIndex >= screens.length ? 0 : _currentIndex,
            children: screens,
          ),
          Positioned(
            top: MediaQuery.of(context).padding.top + 6,
            right: 16,
            child: ScaleInteractiveWidget(
              onTap: () async {
                await Navigator.push(
                  context,
                  MaterialPageRoute(builder: (context) => const ProfileScreen()),
                );
              },
              child: Container(
                padding: const EdgeInsets.all(2),
                decoration: BoxDecoration(
                  shape: BoxShape.circle,
                  border: Border.all(color: Colors.white, width: 2),
                  boxShadow: [
                    BoxShadow(
                      color: Colors.black.withValues(alpha: 0.1),
                      blurRadius: 8,
                      spreadRadius: 1,
                    )
                  ],
                ),
                child: CircleAvatar(
                  radius: 20,
                  backgroundColor: AppColors.navyLight,
                  backgroundImage: (!kIsWeb && Platform.environment.containsKey('FLUTTER_TEST'))
                      ? null
                      : NetworkImage(
                          'https://api.dicebear.com/7.x/avataaars/png?seed=${Uri.encodeComponent(_userName)}',
                        ),
                  child: (!kIsWeb && Platform.environment.containsKey('FLUTTER_TEST'))
                      ? const Icon(Icons.person, color: Colors.white, size: 20)
                      : null,
                ),
              ),
            ),
          ),
        ],
      ),
      bottomNavigationBar: Container(
        decoration: BoxDecoration(
          boxShadow: [
            BoxShadow(
              color: Colors.black.withValues(alpha: 0.04),
              blurRadius: 10,
              offset: const Offset(0, -4),
            )
          ],
        ),
        child: ClipRRect(
          child: BackdropFilter(
            filter: ImageFilter.blur(sigmaX: 15, sigmaY: 15),
            child: BottomNavigationBar(
              currentIndex: _currentIndex >= screens.length ? 0 : _currentIndex,
              onTap: (index) {
                HapticFeedback.selectionClick();
                setState(() {
                  _currentIndex = index;
                });
                // Try to log screen view to Firebase Analytics
                try {
                  String screenName = 'ReportIssue';
                  if (index == 1) screenName = 'LiveMap';
                  if (index == 2) screenName = 'Insights';
                  if (index == 3) screenName = 'Dispatch';
                  FirebaseAnalytics.instance.logEvent(
                    name: 'screen_view',
                    parameters: {
                      'screen_name': screenName,
                    },
                  );
                } catch (_) {}
              },
              backgroundColor: Colors.white,
              selectedItemColor: AppColors.orange,
              unselectedItemColor: const Color(0xFF94A3B8),
              selectedLabelStyle: const TextStyle(
                fontWeight: FontWeight.w700,
                fontSize: 11,
                letterSpacing: 0.2,
              ),
              unselectedLabelStyle: const TextStyle(fontSize: 11),
              iconSize: 24,
              elevation: 0,
              type: BottomNavigationBarType.fixed,
              items: [
                const BottomNavigationBarItem(
                  icon: Icon(Icons.campaign_outlined),
                  activeIcon: Icon(Icons.campaign_rounded),
                  label: 'Report',
                ),
                const BottomNavigationBarItem(
                  icon: Icon(Icons.public_outlined),
                  activeIcon: Icon(Icons.public_rounded),
                  label: 'Live Map',
                ),
                const BottomNavigationBarItem(
                  icon: Icon(Icons.bar_chart_outlined),
                  activeIcon: Icon(Icons.bar_chart_rounded),
                  label: 'Insights',
                ),
                if (_isAdmin)
                  const BottomNavigationBarItem(
                    icon: Icon(Icons.local_police_outlined),
                    activeIcon: Icon(Icons.local_police),
                    label: 'Dispatch',
                  ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
